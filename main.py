async def planner_node(state: DMState, config: RunnableConfig):
    sys_msg = SystemMessage(content="""
        You are the D&D Tactical Planner. You are invisible to the player.
        Your ONLY job is to translate the player's intent into Tool Calls.
        
        TOOL ROUTING GUIDE:
        1. MELEE WEAPONS: Always use `execute_melee_attack`. Never roll manually.
        2. SPELLS & CLASS FEATURES: Always use `use_ability_or_spell`. Never roll manually.
        3. SKILL CHECKS & SAVES: Use `perform_ability_check_or_save` for jumping, sneaking, perception, etc.
        4. DAMAGE/HEALING: Use `modify_health` for falling damage, trap damage, or drinking potions.
        
        If you get a CACHE MISS on a spell or ability, use `query_rulebook` to find the rules, 
        then `encode_new_compendium_entry` to permanently save it to the engine.
        
        Once all tool logic is complete and you have the "MECHANICAL TRUTH", 
        output a brief summary of the events. DO NOT write dialogue or narrative prose.
        """)    
    llm_with_tools = draft_llm.bind_tools(MASTER_TOOLS_LIST)
    response = await llm_with_tools.ainvoke([sys_msg] + state["messages"], config=config)
    return {"messages": [response]}

async def narrator_node(state: DMState, config: RunnableConfig):
    """The Storyteller. Turns the mechanical truth into vivid prose."""
    
    # 1. Check if we are in a revision loop
    feedback_context = ""
    if state.get("qa_feedback") and state["qa_feedback"] != "APPROVED":
        feedback_context = f"\n\n[QA REJECTION FEEDBACK]: Your previous draft was rejected for the following reason:\n{state['qa_feedback']}\nFix this in your new draft."

    sys_msg = SystemMessage(content=f"""
    You are the Dungeon Master. Read the history of the current interaction.
    Look at the 'MECHANICAL TRUTH' outputs generated by the system tools.
    Narrate these exact events vividly to the player. 
    DO NOT change the numbers, damage, or hit/miss outcomes. Do not roll dice.
    Output only the narrative response.
    Do not violate player agency or do anything more than add color to an action dialogue that the player dictated. {feedback_context}
    """)
    
    # Invoke WITHOUT tools so it is forced to write a string
    response = await draft_llm.ainvoke([sys_msg] + state["messages"], config=config)
    return {"draft_response": response.content}

def build_graph():
    workflow = StateGraph(DMState)

    workflow.add_node("planner", planner_node)
    
    # ADD YOUR OTHER TOOLS HERE
    workflow.add_node("action", ToolNode(MASTER_TOOLS_LIST))
    
    workflow.add_node("narrator", narrator_node)
    workflow.add_node("qa", qa_node)

    workflow.set_entry_point("planner")
    workflow.add_conditional_edges("planner", tools_condition, {"tools": "action", "__end__": "narrator"})
    workflow.add_edge("action", "planner")
    workflow.add_edge("narrator", "qa")
    workflow.add_conditional_edges("qa", qa_router)

    return workflow.compile(checkpointer=MemorySaver())


