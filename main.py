@app.middleware("http")
async def log_requests(request: Request, call_next):
    start_time = time.time()
    print(f"\n---> Incoming Request: {request.method} {request.url}")
    
    try:
        response = await call_next(request)
        process_time = time.time() - start_time
        print(f"<--- Response: {response.status_code} (Took {process_time:.2f}s)")
        return response
    except Exception as e:
        print(f"<--- Server Error: {str(e)}")
        raise

class ChatRequest(BaseModel):
    message: str
    character: str
    vault_path: str

class ChatResponse(BaseModel):
    reply: str
    status: str

@app.post("/chat", response_model=ChatResponse)
async def chat_endpoint(request: ChatRequest): # <--- Added async
    global dm_engine_app
    if not dm_engine_app: raise HTTPException(status_code=500, detail="DM Engine not initialized.")
    
    clean_msg = request.message[1:].strip() if request.message.startswith(">") else request.message
    prefix = f"[OOC Question from {request.character}]:" if request.message.startswith(">") else f"[{request.character} acts/speaks]:"
    formatted_prompt = f"{prefix} {clean_msg}"

    print(f"\nInitiating Supervisor Loop for: {formatted_prompt}")
    await write_audit_log(request.vault_path, request.character, "Input Received", clean_msg)
    
    initial_state = {
        "messages": [HumanMessage(content=formatted_prompt)],
        "vault_path": request.vault_path,
        "active_character": request.character,
        "draft_response": "",
        "qa_feedback": "",
        "revision_count": 0
    }
    
    config = {"configurable": {"thread_id": request.vault_path}}

    try:
        # NEW: Initialize the Deterministic Engine from Obsidian before we start thinking
        await initialize_engine_from_vault(request.vault_path)

        # NEW: Await the asynchronous invocation!
        final_state = await dm_engine_app.ainvoke(initial_state, config=config)
        
        # NEW: Save the results of any combat math back to Obsidian files
        await sync_engine_to_vault()
        
        return ChatResponse(reply=final_state["draft_response"], status="success")
        
    except Exception as e:
        print("\n" + "="*40 + "\n💥 FATAL ERROR DURING SUPERVISOR EXECUTION 💥\n" + "="*40)
        traceback.print_exc()
        print("="*40 + "\n")
        raise HTTPException(status_code=500, detail=str(e))

